function cargarform(id,nombre,descripcion,precio,cantidad,categoria,img,estado){
    
    $("#selEstado").val(estado);
    $("#Id").val(id);
    $("#txtNombre").val(nombre);
    $("#txtDescricion").val(descripcion);
    $("#selImagen").val(img);
    $("#selEstado").val(estado);
    $("#txtPrecio").val(precio);
    $("#txtCatidad").val(cantidad);
    $("#selCategoria").val(categoria);
}        