package modelo;

import java.sql.Date;

/**
 *
 * @author TOBAR
 */
public class Pedido {
    private int id;
    private Date fecha;
    private String estado;
    private int idMesa;
    private int idUsuario;

    public Pedido(int id, Date fecha, String estado, int idMesa, int idUsuario) {
        this.id = id;
        this.fecha = fecha;
        this.estado = estado;
        this.idMesa = idMesa;
        this.idUsuario = idUsuario;
    }

    public Pedido() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public int getIdMesa() {
        return idMesa;
    }

    public void setIdMesa(int idMesa) {
        this.idMesa = idMesa;
    }

    public int getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(int idUsuario) {
        this.idUsuario = idUsuario;
    } 
    
}
